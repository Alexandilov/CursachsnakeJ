package project.snakegame;

/**
 * Перечисление {@code Direction} представляет возможные направления движения змейки в игре.
 */
enum Direction {

    /**
     * Движение влево.
     */
    LEFT,

    /**
     * Движение вправо.
     */
    RIGHT,

    /**
     * Движение вверх.
     */
    UP,

    /**
     * Движение вниз.
     */
    DOWN
}

